# Definieer de Celsius- en Fahrenheit-variabelen
c1 = -12
f1 = 144

# Reken Celsius naar Fahrenheit om
fahrenheit1 = (c1 * 9/5) + 32

# Reken Fahrenheit naar Celsius om
celsius1 = (f1 - 32) * 5/9

# Geef de resultaten weer
print(f"{c1} graden Celsius is gelijk aan {fahrenheit1:.1f} graden Fahrenheit")
print(f"{f1} graden Fahrenheit is gelijk aan {celsius1:.1f} graden Celsius")

